    private void txtValorOrcamentoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorOrcamentoKeyTyped
        char c = evt.getKeyChar();
        if ((c < '0' || c > '9') && (c != KeyEvent.VK_BACK_SPACE && c != KeyEvent.VK_DELETE
                && c != KeyEvent.VK_ENTER && c != KeyEvent.VK_PERIOD)) {

            evt.consume();
            JOptionPane.showMessageDialog(this, "Digite apenas números");

            txtValorOrcamento.setText("");
        }
    }//GEN-LAST:event_txtValorOrcamentoKeyTyped


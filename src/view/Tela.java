    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if (txtValorOrcamento.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, insira o valor do orçamento!");
            return;
        }
        if (boxCategoria.getSelectedItem() == "<Selecione>") {
            JOptionPane.showMessageDialog(null, "Por favor, selecione a categoria da despesa");
            return;
        }
        if (txtValorDespesa.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, insira o valor da despesa!");
            return;
        }

        abas.setEnabledAt(2, true);
        int a;
        a = JOptionPane.showConfirmDialog(null, "Deseja cadastrar Despesa?", "Cadastrar",
                JOptionPane.YES_NO_OPTION);

        if (a == JOptionPane.YES_OPTION) {
            if(txtValorOrcamento.getText().trim().equals("")) {
                txtValorOrcamento.setEditable(true);
            } else {
                txtValorOrcamento.setEditable(false);
            }
            
            Integer nomeDespesa = boxCategoria.getSelectedIndex();
            String tipoDespesa = (String) boxTipoDespesa.getSelectedItem();
            Double valorDespesa = Double.parseDouble(txtValorDespesa.getText());
            Despesa des = new Despesa();

            des.cadastrarDespesa(tipoDespesa, nomeDespesa, valorDespesa);

            JOptionPane.showMessageDialog(null, "Despesa cadastrada com sucesso!");

            String categoria = (String) boxCategoria.getSelectedItem();
            String tipoDesp = (String) boxTipoDespesa.getSelectedItem();
            String valor = txtValorDespesa.getText();

            DefaultTableModel tbListagem = (DefaultTableModel) tblListagem.getModel();
            tbListagem.addRow(new String[]{categoria, tipoDesp, valor});

            boxCategoria.setSelectedItem("<Selecione>");
            txtValorDespesa.setText("");

            int b = JOptionPane.showConfirmDialog(null, "Deseja cadastrar nova Despesa?", "Novo",
                    JOptionPane.YES_NO_OPTION);

            if (b == JOptionPane.NO_OPTION) {
                abas.setSelectedIndex(2);

                des.visualizarDespesa();

            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


    private void boxCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxCategoriaActionPerformed

        if (boxCategoria.getSelectedItem() == "<Selecione>") {
            boxTipoDespesa.removeAllItems();

        } else {
            switch ((String) boxCategoria.getSelectedItem()) {

                case "Compras":
                    boxTipoDespesa.removeAllItems();
                    boxTipoDespesa.addItem("Roupas");
                    boxTipoDespesa.addItem("Calçados");
                    boxTipoDespesa.addItem("Cosméticos");
                    break;

                case "Contas":
                    boxTipoDespesa.removeAllItems();
                    boxTipoDespesa.addItem("Água");
                    boxTipoDespesa.addItem("Energia");
                    boxTipoDespesa.addItem("Mercado");
                    boxTipoDespesa.addItem("Gás");
                    break;

                case "Lazer":
                    boxTipoDespesa.removeAllItems();
                    boxTipoDespesa.addItem("Cinema");
                    boxTipoDespesa.addItem("Shows");
                    boxTipoDespesa.addItem("Outros");
                    break;

                case "Viagem":
                    boxTipoDespesa.removeAllItems();
                    boxTipoDespesa.addItem("Transporte");
                    boxTipoDespesa.addItem("Hospedagem");
                    boxTipoDespesa.addItem("Passeios");
                    break;

                case "Outros":
                    boxTipoDespesa.removeAllItems();
                    boxTipoDespesa.addItem("Outros");
                    break;
            }
        }

    }//GEN-LAST:event_boxCategoriaActionPerformed


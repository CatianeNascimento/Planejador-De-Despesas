    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if ((tblListagem.getSelectedRowCount() == 0)) {
            JOptionPane.showMessageDialog(null, "Selecione a linha que deseja Editar!!");
            return;
        }

        int cont = tblListagem.getRowCount();

        String categoria = null, tipoDespesa = null, valor = null;

        for (int i = 0; i < cont; i++) {
            categoria = (String) tblListagem.getValueAt(i, 0);
            tipoDespesa = (String) tblListagem.getValueAt(i, 1);
            valor = (String) tblListagem.getValueAt(i, 2);

        }

        boxCategoria1.setSelectedItem(categoria);
        boxTipoDespesa1.setSelectedItem(tipoDespesa);
        txtValorDespesa1.setText(valor);

        Despesa des = new Despesa();

        des.verificaDespesa(tipoDespesa);

        abas.setSelectedIndex(3);

        ((DefaultTableModel) tblListagem.getModel()).removeRow(tblListagem.getSelectedRow());


    }//GEN-LAST:event_btnEditarActionPerformed

